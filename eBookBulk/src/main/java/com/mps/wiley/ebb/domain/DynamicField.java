package main.java.com.mps.wiley.ebb.domain;


import java.io.Serializable;


public class DynamicField implements Serializable
{
	
	private static final long serialVersionUID = -8115907366174881654L;
	
	private Integer fieldId;
	
	private String fieldName;
	
	private String fieldType;
	
	private String fieldMandatory;
	
	private String fieldLabel;
	
	private Integer fieldOrder=0;
	
	public String getFieldLabel()
	{
		return fieldLabel;
	}

	public void setFieldLabel(String fieldLabel)
	{
		this.fieldLabel = fieldLabel;
	}

	public Integer getFieldOrder()
	{
		return fieldOrder;
	}

	public void setFieldOrder(Integer fieldOrder)
	{
		this.fieldOrder = fieldOrder;
	}



	public String getFieldMandatory()
	{
		return fieldMandatory;
	}
	
	public void setFieldMandatory(String fieldMandatory)
	{
		this.fieldMandatory = fieldMandatory;
	}
	
	public static long getSerialversionuid()
	{
		return serialVersionUID;
	}
	
	
	public String getFieldType()
	{
		return fieldType;
	}
	
	public void setFieldType(String fieldType)
	{
		this.fieldType = fieldType;
	}
	
	public Integer getFieldId()
	{
		return fieldId;
	}
	
	public void setFieldId(Integer fieldId)
	{
		this.fieldId = fieldId;
	}
	
	public String getFieldName()
	{
		return fieldName;
	}
	
	public void setFieldName(String fieldName)
	{
		this.fieldName = fieldName;
	}
	
	
	public DynamicField()
	{
	}

	
}
